public abstract class CalcActions {
    public static int Minus(int a, int b){
        return a - b;
    }
    public static int Plus(int a, int b){ return a + b; }
    public static int Umn(int a, int b){
        return a * b;
    }
    public static int Del(int a, int b){
        return a / b;
    }
}
