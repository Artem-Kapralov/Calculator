public abstract class CalcActions {
    public static int Minus(int a, int b){
        return a - b;
    }
    public static int Plus(int a, int b){
        return a + b;
    }
    public static int Umn(int a, int b){
        return a * b;
    }
    public static double Del(double a, double b){
        return a / b;
    }
}
